print("1.{:d}".format(515))     #정수 표현
print("2.{:5d}".format(515))    #최소 5칸을 차지하고 정수를 넣는다
print("3.{:+5d}".format(515))   #왼쪽 5칸을 차지하고 양수면 +를 표시한다
print("4.{:+d}".format(515))    #양수면 +를 표시한다
print("5.{:=+5d}".format(515))  #왼쪽 5칸을 차지하고 +를 맨 앞에 표시한다.
print("5.{:=+d}".format(515))   #+를 맨 앞에 표시한다.
print("6.{:05d}".format(515))   #빈칸은 0으로 채운다.
print("7.{:+05d}".format(515))  #양수면 0 앞에 +를 표시한다.

